<script>
import { mapActions, mapState } from "vuex";
import Breadcomp from "@/components/Breadcrumb.vue";
// @ is an alias to /src
export default {
  name: "RepairedPallet",
  components: {
    Breadcomp,
  },
  data() {
    return {
      totalDataDownload: 0,
      dialogExport: false,
      downloadRange: [],
      selectedItem: 1,
      headers: [
        { value: "trx_number", text: this.$t("repairPallet.trxNumber") },
        { value: "company_name", text: this.$t("repairPallet.company") },
        { value: "reporter_name", text: this.$t("repairPallet.reporter") },
        { value: "qty_good_pallet", text: this.$t("pallet.good") },
        { value: "note", text: this.$t("repairPallet.note") },
      ],
      filters: {
        trx_number: [],
        company_name: [],
        reporter_name: [],
        qty_good_pallet: [],
        note: [],
      },
      search: "",
      adds: { route: "/repaired-pallet/add" },
      edits: { route: "/repaired-pallet/edit" },
      json_fields: {
        "Transaction Number": "trx_number",
        "Company": "company_name",
        "Reporter": "reporter_name",
        "Quantity Good Pallet": "qty_good_pallet",
        "Note": "note",
      },
    };
  },
  created() {
    this.getRepairedPallets(); //LOAD DATA SJP KETIKA COMPONENT DI-LOAD
  },
  computed: {
    ...mapState("repairedPallet", {
      repairedPallets: (state) => state.repairedPallets, //MENGAMBIL DATA CUSTOMER DARI STATE CUSTOMER
      exportData: (state) => state.exportData, //MENGAMBIL DATA CUSTOMER DARI STATE CUSTOMER
    }),
    ...mapState("repairedPallet", {
      loading: (state) => state.loading, //MENGAMBIL DATA CUSTOMER DARI STATE CUSTOMER
    }),
    filteredRepairedPallets() {
      return this.repairedPallets.filter((d) => {
        return Object.keys(this.filters).every((f) => {
          return this.filters[f].length < 1 || this.filters[f].includes(d[f]);
        });
      });
    },
    dateRangeText () {
      return this.downloadRange.join(' ~ ')
    },
  },
  methods: {
    ...mapActions("repairedPallet", ["getRepairedPallets", "getExportDamagedPallets","deleteRepairedPallet"]),
    columnValueList(val) {
      return this.repairedPallets.map((d) => d[val]);
    },
    editData(item) {
      // Logika untuk mengedit data
      console.log("Mengedit data:", item);
    },
    sumTotal(data) {
      return data.reduce((acc, item) => acc + item.quantity, 0);
    },
    hapusData(item) {
      this.$swal({
        title: "Are you sure?",
        text: "This will delete record Permanently!",
        type: "warning",
        showCancelButton: true,
        confirmButtonColor: "#3085d6",
        cancelButtonColor: "#d33",
        confirmButtonText: "Yes!",
      }).then((result) => {
        if (result.value) {
          this.deleteRepairedPallet(item.id); //JIKA SETUJU MAKA PERMINTAAN HAPUS AKAN DI EKSEKUSI
        }
      });
    },
    getExportData() {
      this.getExportDamagedPallets(this.downloadRange).then((result) => {
        this.totalDataDownload = result.data.length
      });
    },
  },
};
</script>
